struct node
{
    int data;
    struct node* next;
    
    node(int x){
        data = x;
        next = NULL;
    }
    
};

/* Function to print linked list */
void printList(struct node *node)
{
    while (node != NULL)
    {
        printf("%d ", node->data);
        node = node->next;
    }
    printf("\n");
}


 // } Driver Code Ends


class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
        
        int n=0;
        struct node* prev = NULL;
        struct node * curr=head,*nexe=head;
        
        while(curr!=NULL&&n<k)
        {
            nexe=curr->next;
            curr->next=prev;
            prev=curr;
            curr=nexe;
            n++;
        }
        if(nexe!=NULL)
        {
            head->next=reverse(nexe,k);
        }
        return prev;
    }
};


